export const getRandomPairings = (
  competition: keyof typeof teams,
): [Team, Team][] => {
  const shuffledTeams = shuffle(teams[competition]);
  const pairings: [Team, Team][] = [];

  for (let i = 0; i < shuffledTeams.length; i += 2) {
    pairings.push([shuffledTeams[i], shuffledTeams[i + 1]]);
  }

  return pairings.slice(0, 5);
};

const shuffle = <T>(array: T[]) => {
  const shuffledArray = [...array];

  for (let i = shuffledArray.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * i);
    const temp = shuffledArray[i];
    shuffledArray[i] = shuffledArray[j];
    shuffledArray[j] = temp;
  }

  return shuffledArray;
};
